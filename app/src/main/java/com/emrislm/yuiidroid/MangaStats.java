package com.emrislm.yuiidroid;

public class MangaStats {

    private int reading;
    private int completed;
    private int on_hold;
    private int dropped;
    private int plan_to_read;
    private int total;

    public int getReading() {
        return reading;
    }
    public void setReading(int reading) {
        this.reading = reading;
    }

    public int getCompleted() {
        return completed;
    }
    public void setCompleted(int completed) {
        this.completed = completed;
    }

    public int getOn_hold() {
        return on_hold;
    }
    public void setOn_hold(int on_hold) {
        this.on_hold = on_hold;
    }

    public int getDropped() {
        return dropped;
    }
    public void setDropped(int dropped) {
        this.dropped = dropped;
    }

    public int getPlan_to_read() {
        return plan_to_read;
    }
    public void setPlan_to_read(int plan_to_read) {
        this.plan_to_read = plan_to_read;
    }

    public int getTotal() {
        return total;
    }
    public void setTotal(int total) {
        this.total = total;
    }
}
