package com.emrislm.yuiidroid;

public class MangaCharacters {

    private String characterImg;
    private String characterName;
    private String characterRole;

    public String getCharacterImg() {
        return characterImg;
    }
    public void setCharacterImg(String characterImg) {
        this.characterImg = characterImg;
    }

    public String getCharacterName() {
        return characterName;
    }
    public void setCharacterName(String characterName) {
        this.characterName = characterName;
    }

    public String getCharacterRole() {
        return characterRole;
    }
    public void setCharacterRole(String characterRole) {
        this.characterRole = characterRole;
    }
}
